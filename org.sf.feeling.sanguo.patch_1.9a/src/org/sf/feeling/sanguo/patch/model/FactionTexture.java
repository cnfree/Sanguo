
package org.sf.feeling.sanguo.patch.model;

public class FactionTexture
{

	private String standard_texture;
	private String rebels_texture;
	private String routing_texture;
	private String ally_texture;

	public String getStandard_texture( )
	{
		return standard_texture;
	}

	public void setStandard_texture( String standard_texture )
	{
		this.standard_texture = standard_texture;
	}

	public String getRebels_texture( )
	{
		return rebels_texture;
	}

	public void setRebels_texture( String rebels_texture )
	{
		this.rebels_texture = rebels_texture;
	}

	public String getRouting_texture( )
	{
		return routing_texture;
	}

	public void setRouting_texture( String routing_texture )
	{
		this.routing_texture = routing_texture;
	}

	public String getAlly_texture( )
	{
		return ally_texture;
	}

	public void setAlly_texture( String ally_texture )
	{
		this.ally_texture = ally_texture;
	}
}
